#!/bin/bash
# gtr-serena-register.sh - Register worktree with Serena MCP
# Usage: gtr-serena-register.sh <worktree_path> <repo_name> <issue_number>
#
# This script creates worktree-isolated Serena configuration:
# 1. Creates .serena/project.yml with unique project name
# 2. Creates .serena/serena_config.yml for worktree-specific settings
# 3. Creates .mcp.json with SERENA_HOME pointing to worktree's .serena/
#
# This ensures Serena operates on the worktree, not master.
# Called by gtr-start after worktree creation

set -e

WORKTREE_PATH="$1"
REPO_NAME="$2"
ISSUE_NUMBER="$3"

# Validation
if [ -z "$WORKTREE_PATH" ] || [ -z "$REPO_NAME" ] || [ -z "$ISSUE_NUMBER" ]; then
    echo "Usage: gtr-serena-register.sh <worktree_path> <repo_name> <issue_number>"
    exit 1
fi

if [ ! -d "$WORKTREE_PATH" ]; then
    echo "Error: Worktree path does not exist: $WORKTREE_PATH"
    exit 1
fi

PROJECT_NAME="${REPO_NAME}-wt-issue-${ISSUE_NUMBER}"

# Detect parent (master) repo
PARENT_REPO=$(cd "$WORKTREE_PATH" && git rev-parse --show-superproject-working-tree 2>/dev/null || git worktree list --porcelain | grep '^worktree ' | head -1 | sed 's/^worktree //')

# 1. Create .serena directory
mkdir -p "${WORKTREE_PATH}/.serena"

# 1.5. Copy memories from master repo (if exists)
if [ -n "$PARENT_REPO" ] && [ -d "${PARENT_REPO}/.serena/memories" ]; then
    cp -r "${PARENT_REPO}/.serena/memories" "${WORKTREE_PATH}/.serena/"
    echo "   📝 Copied memories from master"
fi

# Detect language from parent repo if possible
PARENT_PROJECT_YML=""
if [ -n "$PARENT_REPO" ] && [ -f "${PARENT_REPO}/.serena/project.yml" ]; then
    PARENT_PROJECT_YML="${PARENT_REPO}/.serena/project.yml"
fi

# Get language from parent or default to python
if [ -n "$PARENT_PROJECT_YML" ]; then
    LANGUAGES=$(grep -A1 '^languages:' "$PARENT_PROJECT_YML" | tail -1 | sed 's/^- //')
else
    LANGUAGES="python"
fi

# 2. Create .serena/project.yml with unique project name
cat > "${WORKTREE_PATH}/.serena/project.yml" << EOF
# Auto-generated by gtr-serena-register.sh
# For worktree: issue-${ISSUE_NUMBER}

languages:
- ${LANGUAGES}

encoding: "utf-8"
ignore_all_files_in_gitignore: true
read_only: false
excluded_tools: []
initial_prompt: ""

project_name: "${PROJECT_NAME}"
included_optional_tools: []
EOF

# 3. Create .serena/serena_config.yml for worktree-specific settings
cat > "${WORKTREE_PATH}/.serena/serena_config.yml" << EOF
# Worktree-specific Serena configuration
# SERENA_HOME points here via .mcp.json
projects:
- ${WORKTREE_PATH}
language_backend: LSP
tool_timeout: 240
gui_log_window: false
web_dashboard: false
web_dashboard_open_on_launch: false
EOF

# 4. Create .mcp.json with SERENA_HOME pointing to worktree's .serena/
cat > "${WORKTREE_PATH}/.mcp.json" << EOF
{
  "mcpServers": {
    "serena": {
      "command": "uvx",
      "args": [
        "--from", "git+https://github.com/oraios/serena",
        "serena", "start-mcp-server",
        "--project-from-cwd"
      ],
      "env": {
        "SERENA_HOME": "${WORKTREE_PATH}/.serena"
      }
    }
  }
}
EOF

echo "   ✅ Serena registered: ${PROJECT_NAME}"
echo "      Path: ${WORKTREE_PATH}"
echo "      SERENA_HOME: ${WORKTREE_PATH}/.serena"
