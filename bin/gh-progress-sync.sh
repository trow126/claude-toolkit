#!/bin/bash
# gh-progress-sync.sh - GitHub Issue進捗同期
# Usage: echo '{"issue": 42, "completed": [1,2], "total": 5, "task_name": "Impl"}' | gh-progress-sync.sh
#        gh-progress-sync.sh --check-task 42 "Task 3"
#
# GitHub Issueのチェックボックスを更新し、進捗コメントを投稿

set -e

show_help() {
    echo "gh-progress-sync.sh - GitHub Issue進捗同期"
    echo ""
    echo "Usage:"
    echo "  echo '{JSON}' | gh-progress-sync.sh   # stdin から JSON を受け取る"
    echo "  gh-progress-sync.sh --check-task <issue> <task-text>  # タスクをチェック"
    echo ""
    echo "JSON format:"
    echo '  {"issue": 42, "completed": [1,2], "total": 5, "task_name": "Implementation"}'
    echo ""
    echo "Options:"
    echo "  --check-task <issue> <text>  # 指定タスクをチェック済みにマーク"
    echo ""
    echo "Examples:"
    echo '  echo '\''{"issue": 42, "completed": [1], "total": 3, "task_name": "Add login"}'\'' | gh-progress-sync.sh'
    echo '  gh-progress-sync.sh --check-task 42 "Add login button"'
}

# ヘルプ
case "$1" in
    -h|--help)
        show_help
        exit 0
        ;;
esac

# --check-task モード: 特定タスクをチェック済みにする
if [[ "$1" == "--check-task" ]]; then
    ISSUE_NUMBER="$2"
    TASK_TEXT="$3"

    if [[ -z "$ISSUE_NUMBER" ]] || [[ -z "$TASK_TEXT" ]]; then
        echo "Error: --check-task requires issue number and task text" >&2
        show_help >&2
        exit 1
    fi

    # Issue body取得
    BODY=$(gh issue view "$ISSUE_NUMBER" --json body -q '.body')

    # タスクをチェック済みに更新（正規表現置換）
    # "- [ ] ${TASK_TEXT}" → "- [x] ${TASK_TEXT}"
    ESCAPED_TEXT=$(echo "$TASK_TEXT" | sed 's/[]\/$*.^[]/\\&/g')
    NEW_BODY=$(echo "$BODY" | sed "s/- \[ \] ${ESCAPED_TEXT}/- [x] ${ESCAPED_TEXT}/g")

    if [[ "$BODY" == "$NEW_BODY" ]]; then
        echo '{"status": "no_change", "message": "Task not found or already checked"}' >&2
        exit 0
    fi

    # Issue更新
    gh issue edit "$ISSUE_NUMBER" --body "$NEW_BODY"
    echo '{"status": "updated", "task": "'"$TASK_TEXT"'"}'
    exit 0
fi

# JSON モード: stdin から進捗データを受け取る
if [[ -t 0 ]]; then
    echo "Error: No JSON input. Use stdin or --check-task mode." >&2
    show_help >&2
    exit 1
fi

read -r INPUT

# JSONパース
ISSUE_NUMBER=$(echo "$INPUT" | jq -r '.issue // empty')
COMPLETED_COUNT=$(echo "$INPUT" | jq -r '.completed | length // 0')
TOTAL=$(echo "$INPUT" | jq -r '.total // 0')
TASK_NAME=$(echo "$INPUT" | jq -r '.task_name // "Task"')

# バリデーション
if [[ -z "$ISSUE_NUMBER" ]]; then
    echo '{"error": "Missing issue number in JSON"}' >&2
    exit 1
fi

if [[ "$TOTAL" -eq 0 ]]; then
    echo '{"error": "Total tasks cannot be zero"}' >&2
    exit 1
fi

# 進捗計算
PERCENTAGE=$((COMPLETED_COUNT * 100 / TOTAL))

# 進捗コメント投稿
COMMENT_BODY=$(cat <<EOF
**Task Completed**: ${TASK_NAME}

**Progress**: ${COMPLETED_COUNT}/${TOTAL} tasks (${PERCENTAGE}%)

---
_Auto-generated by gh-progress-sync_
EOF
)

gh issue comment "$ISSUE_NUMBER" --body "$COMMENT_BODY" >/dev/null

# 全完了チェック
if [[ "$COMPLETED_COUNT" -eq "$TOTAL" ]]; then
    echo '{"status": "all_complete", "percentage": 100, "message": "All tasks complete. Consider creating PR with Closes #'"${ISSUE_NUMBER}"'"}'
else
    echo '{"status": "synced", "percentage": '"${PERCENTAGE}"', "remaining": '$((TOTAL - COMPLETED_COUNT))'}'
fi
